
public class ElementaryProgramming2 {
	
	public static void main(String[] args) {
		
			double a = 30; // 30.0 // 64-bit
			double b = 4; // 4.0 // 64-bit
			
			System.out.println(a / b);
			
			System.out.println(30 / 4);
			
			System.out.println(30.0 / 4.0);
			
			double c = 9.0;  // 64 -bit
			// int d = 4.0; // CAN NOT
			int d = 4;      // 32 -bit
			
			System.out.println(c / d);
			
			
			// Float --Literal F-f
			float e = 4.0F;
			System.out.println(e);
			// float q = (float)0.0;
			
			
			// Double -- Literal
			double f = 9.0D;
			
			
	}

}
