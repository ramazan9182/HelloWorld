
public class RelationalOperators {

	public static void main(String[] args) {
		
		int a = 45;
		int b = 34;
		
		// a < b
		
		System.out.println(a > b);
		
		// produces boolean result
		
		boolean result = a < b;
		boolean result2 = a <= b;
		boolean result3 = a > b; 
		boolean result4 = a >= b;
		boolean result5 = a == b;
		boolean result6 = a != b;
		
		
	}
}
